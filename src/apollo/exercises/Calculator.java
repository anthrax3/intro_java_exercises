package apollo.exercises;

/*
 * Calculator Application
 * 
 *   Supports a multiply method (takes 2 integers, return product)
 *   Supports a add method (takes 2 integers, return sum)
 *   
 */

public class Calculator {
	// Multiples 2 integers and returns the product.
	public int multiply(int x, int y) {
		return x * y;
	}
	
	// Adds 2 integers and returns the sum.
	public int add(int x, int y) {
		return x + y;
	}
}